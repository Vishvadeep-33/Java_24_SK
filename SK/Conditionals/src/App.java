public class App {
    public static void main(String[] args) throws Exception {
        // control flow statement 
        //  3 - conditional, loop statement, jump statement
        // conditionals 
        // 1 . if....else if....else
        // 2 switch case
        boolean Condition = 10 >= 20;
        if(Condition){
            System.out.println("true");
        }
        else if(!Condition){
            System.out.println("false");
        }
        else{
            System.out.println("false");
        }
        
    }
}
